@push('scripts')
<script>
    function deleteUser(id, name) {
        document.getElementById('delete-user-name').textContent = name;
        document.getElementById('delete-user-form').action = `/admin/users/${id}`;
        document.getElementById('deleteModal').classList.remove('hidden');
    }

    function closeDeleteModal() {
        document.getElementById('deleteModal').classList.add('hidden');
    }

    function editUser(id, name, email, role, premiumType) {
        document.getElementById('user-modal-title').textContent = 'Edit User';
        document.getElementById('password-optional').classList.remove('hidden');
        document.getElementById('userForm').action = `/admin/users/${id}`;
        
        const methodField = document.getElementById('method-field');
        methodField.innerHTML = '<input type="hidden" name="_method" value="PUT">';
        
        document.getElementById('name').value = name;
        document.getElementById('email').value = email;
        document.getElementById('password').required = false;
        document.getElementById('role-select').value = role;
        document.getElementById('premium_type-select').value = premiumType === 'None' ? '' : premiumType;
        
        document.getElementById('addUserModal').classList.remove('hidden');
    }

    function closeUserModal() {
        document.getElementById('addUserModal').classList.add('hidden');
        // Reset form for next use
        document.getElementById('userForm').reset();
        document.getElementById('user-modal-title').textContent = 'Add New User';
        document.getElementById('password-optional').classList.add('hidden');
        document.getElementById('password').required = true;
        document.getElementById('method-field').innerHTML = '';
        document.getElementById('userForm').action = "{{ route('admin.users.store') }}";
    }

    // For the "Add New User" button
    document.addEventListener('DOMContentLoaded', function() {
        const addUserButtons = document.querySelectorAll('[data-modal-toggle="addUserModal"]');
        addUserButtons.forEach(button => {
            button.addEventListener('click', function() {
                closeUserModal(); // Reset the form
                document.getElementById('addUserModal').classList.remove('hidden');
            });
        });
    });
</script>
@endpush
